bool create_pattern(pattern* const n, const char* d)
{
    const unsigned char* p = d;
    size_t i = 0;
    while (*p)
    {
        if (i >= MAX_PATTERN_SIZE)
        {
            return false;
        }

        if (*p == '?')
        {
            n->data[i] = 0x00;
            n->mask[i] = 0x00;
            p++;
            i++;
            continue;
        }

        if (g_hex_table[*p] == 0xFF)
        {
            p++;
            continue;
        }

        unsigned char data = 0;
        data |= ((g_hex_table[*p++] << 4) & 0xF0);
        data |= ((g_hex_table[*p++] << 0) & 0x0F);

        n->data[i] = data;
        n->mask[i] = 0xFF;

        i++;
    }
    n->size = i;
    return true;
}